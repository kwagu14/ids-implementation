/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* SAU/IDAU, FPU and interrupts secure/non-secure allocation setup done */
  /* in SystemInit() based on partition_stm32l562xx.h file's definitions. */
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();
  /* GTZC initialisation */
  MX_GTZC_S_Init();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_SPI3_Init();
  MX_USART1_UART_Init();
  MX_ICACHE_Init();
  /* USER CODE BEGIN 2 */
  int currentNum = 0;
  int counter = 0;
  /* USER CODE END 2 */

  /*************** Setup and jump to non-secure *******************************/

//  NonSecure_Init();

  /* Non-secure software does not return, this code is not executed */
  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	for(int i = 0; i<32; i++){
		aTxBuffer[i] = currentNum+i;
	}
	currentNum+=32;
	counter ++;

	printf("The current buffer is: \n\r");
	for(int i = 0; i<32; i++){
		printf("%d, ", aTxBuffer[i]);
	}
	printf("\n\r");

	/*##-1- Start the Full Duplex Communication process ########################*/
	 /* While the SPI in TransmitReceive process, user can transmit data through
	  "aTxBuffer" buffer & receive data through "aRxBuffer" */
	//for now, just send a simple message
	HAL_GPIO_WritePin(GPIOB, GPIO_PIN_13, GPIO_PIN_RESET);
	printf("Sending buffer to the server...\n\r");
	if (HAL_SPI_TransmitReceive_DMA(&hspi3, (uint8_t *)aTxBuffer, (uint8_t *)aRxBuffer, 32) != HAL_OK)
	 {
		 /* Transfer error in transmission process */
		 printf("There was an error in starting up txrx.\n\r");
		 Error_Handler();
	 }

	 /*##-2- Wait for the end of the transfer ###################################*/
	 /*  Before starting a new communication transfer, you must wait the callback call
	   to get the transfer complete confirmation or an error detection.
	   For simplicity reasons, this example is just waiting till the end of the
	   transfer, but application may perform other tasks while transfer operation
	   is ongoing. */
	 while (wTransferState == TRANSFER_WAIT)
	 {
	 }

	 switch (wTransferState)
	 {
		 case TRANSFER_COMPLETE:
		   break;
		 default :
		   printf("There was an error in SPI transfer.\n\r");
		   Error_Handler();
		   break;
	 }
	 HAL_Delay(100);

	 //check if we've transmitted 8 full buffers
	 if(counter == 8){
		 //stop SPI communication
		 HAL_GPIO_WritePin(GPIOB, GPIO_PIN_13, GPIO_PIN_SET);
		 //reset variables
		 counter = 0;
		 currentNum = 0;
		 for(int i = 0; i<32; i++){
			 aTxBuffer[i] = 0;
		 }
		 //delay 10 seconds before resuming
		 HAL_Delay(10000);
		 printf("The memory transfer was completed.\n\r");
	 }

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

